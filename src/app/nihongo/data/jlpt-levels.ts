/**
 * Look up the JLPT level of a word. Returns 0 if unknown.
 */
export function lookupJLPT(word: string): number {
  return JLPT_MAP[word] || 0;
}

/**
 * Get all words at a specific JLPT level.
 */
export function getWordsByLevel(level: number): string[] {
  return Object.entries(JLPT_MAP)
    .filter(([, l]) => l === level)
    .map(([w]) => w);
}
